<li>Participatory and Social Environmental Media Analysis</li>
<li>Discovery of Environmental Multimedia Information in the web</li>
<li>Content Extraction from Environmental Multimedia Data</li>
<li>Personalised Services based on Environmental Information</li>
<li>Fusion of Multimedia Environmental Information.</li>
<li>Interfaces, Presentation and Visualization tools for Environmental Data</li>
<li>Semantic Web Approaches for Environmental Data</li>
<li>Decision support services and reasoning of Multimedia Environmental Data</li>
<li>Detection and monitoring of marine life</li>
<li>Underwater image enhancement</li>
