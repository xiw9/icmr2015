            <p>EMR 2015 aims at presenting the most recent works in the area of environmental multimedia retrieval.
The advancements in digital technologies have facilitated the multimedia encoding of environmental
information, such as meteorological measurements and nature observation including the marine
environment. In addition, the developments in the area of Citizen Science and Voluntary Geographic
Information call for the collection of environmental multimedia data. Since the analysis of
environmental information is critical both for human activities (e.g. agriculture) and for the
sustainability of the planet (e.g. global warming), it is of great importance to develop techniques for
the analysis and retrieval of environmental multimedia. Of particular interest is the monitoring of
marine ecosystems, which is of critical importance for gaining a better understanding of the effects of
anthropogenic influences on marine environments. In this context EMR 2015 focuses on analysis,
retrieval and semantic interpretation of environmental multimedia data, as well as on personalised
services, visualization and decision support environmental applications for environmental multimedia.
</p>
