            <p>Recommender systems are becoming increasingly important due to the overload of
information brought by todayâ€™s Internet. With the rapid growth of digital devices and 
media  sharing  platforms,  the  amount  of  multimedia  information  has  explosively 
increased in recent years. Therefore, multimedia recommender systems are crucial for
proactively helping users in their information seeking process within the huge volume
of Internet multimedia. In traditional recommender systems, user-item collaborative
information (such as Collaborative Filtering and Matrix Factorization based methods)
and multimedia contents (such as Content-based Filtering methods) are two major
sources to predict the user-item interaction behaviors. However, with the growth of
data scale, traditional recommender systems seriously suffer from the sparsity
problem of user-item interactions. In addition, it has been demonstrated by the
industry (e.g. Netflix, YouTube etc.) that the collaborative information is quite limited
in capturing the user behavior patterns and intentions in information seeking.
</p>
<p>
  With the fast development of social networks and online media sharing platforms,
user profiles, tagged multimedia contents and the interaction behaviors between users
and multimedia contents are digitally recorded in an unprecedented level. This
provide us a precious opportunity to deeply investigate the user-information
interaction mechanism, and design more accurate recommender systems. Take video
recommendation for example. From the video aspect, we have not only video contents,
but also user-labeled tags, geographic information, and user comments etc. From the
user (crowd) aspect, we also have user profiles, user relationships and user
interactions, etc. From the user-information interaction aspect, we can reconstruct
fine-granular user behavior logs and also the temporal and spatial context information
 for these interaction behaviors. The information above gives us a good opportunity to
better understand the user-content interaction behavior patterns. Further, it can be
regarded as a supplement of collaborative information to solve the sparsity problem.
Therefore, developing computational methods to jointly utilize the information in
content, context and crowd dimensions for multimedia recommendation is of
paramount importance to improve the performance of recommender systems, and
provide us new advances including new approaches and directions on multimedia
recommendation.
 </p>
