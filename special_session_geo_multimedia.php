            <h1 class="panel-title">Geo-Multimedia Analytics and Retrieval</h1>
          </div>
          <div class="panel-body" >
            <p>Location based multimedia big data holds greatest potential for fundamentally
changing how information is fast and effectively accessed and presented; how knowledge is
sufficiently learned and extracted; and how business (e.g., branding and customer relationship
management) is practiced.
</p>
            <p>Indeed, over the past few years, it has begun to usher in a new and exciting era of knowledge
discovery and information retrieval research. On the one hand, with a huge volume of Internet
based, mobile and sensor-generated multimedia data reaching to Exabyte scale, how to develop
advanced data mining/information retrieval techniques and systems is becoming more and
more important. Many believe location based multimedia big data analysis and retrieval offers
the scholars from different disciplines a unique opportunity to design, implement and test
methodical approaches and (or) intelligent systems at many different levels using many
different ways. In addition to the underlying technologies, location based multimedia big data
analytics includes location-centric and user-centric methodologies and processes can be applied
to a wide range of novel applications such as market intelligence, e-commerce and urban
planning.
</p>
            <p>This special session is devoted to the publications of high-quality and high-impact research
results on technical developments and practical applications around social, geo-media big data
analysis and retrieval. It will serve as a forum for recent advances in the fields of social and geo-
media content analysis, mining, search, and emerging new applications, such as multimedia big
data analysis for geolocation modeling, geo-media systems, context-aware advertising, and
personalized socio-mobile experience.
</p>
            <h3>Scope and Topics</h3>
            <p>The special session of ICMR 2015 invites unpublished, original research
relating to all aspects of big location based media analytics and retrieval.
Relevant topics include, but are not limited to, the following:
</p>
            <ul>
<li>Contextual models for social geo-media analysis</li>
<li>Techniques, processes, and methods for harvesting location based big multimedia data</li>
<li>Efficient learning and mining algorithms for social geo-media analysis</li>
<li>Cloud support and deep learning for social geo-media analysis and retrieval</li>
<li>Multimedia indexing and mining on mobile devices</li>
<li>Social media interaction and visualization on mobile devices</li>
<li>Location-based media modeling</li>
<li>Social geo-media recommendation</li>
<li>Human-centered geolocation</li>
<li>New applications (e.g., data management system and user modeling)</li>
            </ul>
