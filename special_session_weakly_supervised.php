<li>Discovering new types of weak supervision for computer vision tasks</li>
<li>Weakly-supervised indexing/hashing/ranking techniques for large-scale image and video retrieval</li>
<li>Vision model by learning the spatial-temporal context in social media</li>
<li>Applying location-aware social media to enhance visual search</li>
<li>Different social media visualization techniques</li>
<li>Learning visual semantics for intelligent traffic system</li>
<li>Visual semantic understanding in 3D/stereo data</li>
<li>Statistical models for weakly-supervised learning</li>
<li>Robustly model learning with little or no supervision.
<li>Other related applications on weakly supervised learning.
</ul>
