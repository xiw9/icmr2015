<p>Digital content creation (photos, videos and lots of associated sensor data) is nowadays easier
than ever, resulting in huge amounts of multimedia content created every day. It is also, more
than ever before, a collective experience: a major portion of all the created digital content is
shared via social sharing platforms, and in many cases the envisaged social sharing was the
primary reason for creating the content in the first place. Although most of these shared photos
and videos have been captured during either a public or private event (e.g. a concert, a football
game, a wedding, a birthday party), in the social Web they are often scattered, i.e., they are
disassociated from the related events. This creates a fragmented environment, where finding and
organizing the digital content and related interesting events presents significant challenges. These
are further amplified by the fact that the sensor data, which are typically associated with the
photos and videos, such as time and location information, are (despite common belief or hope)
often wrong, inaccurate or misleading (concerning time information, for instance, such errors may
be due to a wrong setting of the clock/calendar of the image capture device, or to different time-
zone settings). Considering that humans often think in terms of events, devising techniques that
address the above challenges and support the event-based organization of the content,
represents a significant step towards making multimedia browsing and search possible, as well as
social media understanding, in ways that are more efficient and more natural to the users.
</p>
<h3>Scope and Topics</h3>
<p>This Special Session aims to attract and present the latest developments and results on the
discovery of social and personal events from web multimedia content; the detection and retrieval
of user-contributed media collections that are related to such events; the time-synchronization of
multiple media collections captured by different users or devices but corresponding to the same
event; and the time-aligned consumption of multimedia in novel applications such as event
summarization and storyboarding. More specifically, the topics of the special session include:
  </p>
<ul>
<li>Discovery of social events from web multimedia</li>
<li>Discovery of personal events from web multimedia</li>
<li>Associating user-contributed media collections with events</li>
<li>Time-synchronization of event-based media collections</li>
<li>Geo-spatial event analysis</li>
<li>Summarization of large events based on social multimedia</li>
<li>Event-based visualization and user interaction</li>
<li>Event-based retrieval and indexing for mobile applications</li>
<li>Social event analytics</li>
<li>Storyboarding and other novel event-based applications</li>
</ul>
