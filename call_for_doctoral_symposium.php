          <div class="panel-body" >
            <p>The ICMR 2015 Doctoral Symposium plans to bring together Ph.D. students working on topics related to multimedia and information retrieval. The Doctoral Symposium will take place during the main conference in oral presentation format. The goal is to provide a forum for Ph.D. students to present ongoing research in a collaborative environment and to share ideas with other renowned and experienced researchers. Participants will discuss their research ideas and results, and they will receive constructive feedback from an audience consisting of peers as well as more senior people. It will be an excellent opportunity for developing person-to-person networks to the benefit of the community and Ph.D. students in their future careers.</p>
            <h3>Eligibility</h3>
            <p>Prospective student attendees should already have a clear direction for research, and possibly have published some results. Preference will be given to students who have advanced to Ph.D. candidacy.</p>
            <h3>Review Process and Publication</h3>
            <p>Review is single-blind. Selections will be based on a 4 page abstract, singly authored by the student wishing to attend. Submissions will be reviewed by the members of the Doctoral Symposium Committee. Accepted proposals will be published in the proceedings (part of the ACM Digital Library).</p>
            <p>Doctoral students who submit to the Doctoral Symposium are encouraged to submit a paper on their research to the main conference. However, acceptance for participation in the Symposium will be based solely on the abstract written ad hoc for the event. All abstracts will be reviewed with respect to overall quality of presentation, potential for future impact of the research on the field, and expected benefit to the other doctoral students attending the Doctoral Symposium.</p>
            <h3>Submissions</h3>
            Applications to the Doctoral Symposium should include a 4-page abstract summarizing the applicant’s dissertation research. The abstract should include:</p>
            <ul>
            <li>Motivation, problem description</li>
            <li>Background and related work (including key references)</li>
            <li>Novelty and significance relative to the state of the art</li>
            <li>Approach, data, methods and proposed experiments</li>
            <li>Results obtained and work in progress</li>
            <li><em>Specific research issues for discussion at the Doctoral Symposium.</em></li>
            </ul>
            <p>Added to the application, a 1-page appendix should describe the benefits that would be obtained by attending the Symposium, including:</p>
            <ul>
            <li>A statement by the student saying why they want to attend the Symposium.</li>
            <li><em>A statement by their advisor saying how the student would benefit by attending the Symposium. Advisors should also specifically state whether the student has written, or is close to completing, a thesis proposal (or equivalent), and when they expect the student would defend their dissertation if they progress at a typical rate.</em></li>
            </ul>
            <p>Submissions must be written in English and submitted in the ACM Conference style (for LaTeX, use the “Option 2″ style). The paper should be no more than 4 pages in length including all figures and references. The first page must contain the title of the paper, full author name, affiliation and contact details, an abstract of up to 250 words, ACM Computing Reviews categories, and up to 3 keywords describing the topic areas. Information about categories and keywords can be found in the ACM Web pages on the computing classification system and in the LaTeX and Word templates.</p>

