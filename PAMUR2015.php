          <div class="panel-body" >
            <p>Multimedia  retrieval  (MR)  is  primarily  meant to  serve  end users.  Hence,  taking  into  account 
human  factors  is  an   important  aspect  in  the  process  of  MR.  Affect  and  personality  are  such   human 
factors. In  recent  years, the  respective  domains  of  affective computing and  personality computing have 
achieved  such  maturity that  their outcomes  can  be  used in indexing, retrieval, and  in  evaluation of  MR 
systems. 
</p>
<h3>Call for papers</h3>
<p>The workshop topics include, but are not limited to: 
</p>
<ul>
<li>affective analysis/labeling of multimedia content </li>
<li>acquisition of  personality and affect for MR </li>
<li>relationship between personality and MM content through user preferences </li>
<li>personalized and contextualized retrieval or recommendation </li>
<li>affective­ and personality­based MM similarity measures </li>
<li>affective­ and personality­based user interfaces for MR </li>
<li>information visualization of affect and personality data </li>
<li>mood regulation through MM  </li>
<li>evaluation of subjective satisfaction </li>
<li>human factors for MR and recommendation </li>
<li>datasets for affect­ and personality­related retrieval and recommendation </li>
</ul>
<p>We  seek opportunities  to  invite  the most outstanding  accepted papers  to  a journal special issue related 
to the field.  
</p>
<p>Submissions  must  be original and not submitted to or accepted by any other conference or 
journal.  They   should  be  prepared  according   to  the  ICMR  2015  format  (ACM  style)  and 
must  not  exceed  6  pages.  The  reviewing  process  will  be  double  blind.  Therefore,  authors 
must   conceal  their  identity  (no  author  names,  no  affiliations,  no  acknowledgment  of 
sponsors,  no  direct  references  to  previous  work).  Each  paper  will  receive  at  least  3 
reviews  by  recognized  experts  in  the  field.  Please  use   EasyChair  to  submit  your 
manuscripts.
</p>

