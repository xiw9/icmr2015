            <h3>Tutorial Description</h3>
            <p>Recently we have witnessed an explosive growth of image/video/audio data in both 
the local centers and social-networking websites, such as Flickr, YouTube, and 
Facebook. Artificial intelligence techniques have proven useful for interpreting this 
preponderance of data. In the last decades, many quality models have been proposed.
Computational quality models evaluate multimedia contents either objectively or 
subjectively, based on which large-scale multimedia content can be managed efficiently. 
They are useful tools in various applications such as multimedia retrieval, 
recommendation systems, graphical design, and etc. Building a successful quality 
model depends on a wide range of domain knowledge, such as multimedia, computer 
vision, machine learning, and even cognitive science. Extensive research efforts have 
been dedicated to design multimedia quality models. While effective methods to 
manipulate this task are still at their infancy. In detail, some key technical challenges are: 
1) the deemphasized role of semantic content that are many times more important than 
low-level visual features in media quality prediction; 2) the necessity to incorporate 
human perception of multimedia contents (e.g., biologically-inspired visual/acoustic 
features) for quality assessment; 3) the difficulty to optimally fuse low-level and high-
level visual features into a quality model; and 4) the lack of publicly available data sets 
to fairly evaluate the performance of a specific quality model. This tutorial targets the 
recent technical theory and applications on computational models, such as photo/video 
quality-based retargeting and feature selection for multimedia retrieval. A brief outline 
of our tutorial can be described as follows:</p>
<ul>
  <li>New computational models for image and video quality evaluation (by Luming Zhang);</li>
  <li>Applications closely related to computational quality models, such as image
cropping/retargeting, video summarization, and feature selection for efficient 
multimedia retrieval (by Luming Zhang);</li>
  <li>Discovering biologically/psychologically-inspired visual features for computational
quality models (by Luming Zhang);</li>
  <li>Novel feature selection algorithms for multimedia analysis (by Yi Yang);</li>
  <li>State-of-the-art feature engineering techniques for multimedia event detection
(by Yi Yang);</li>
  </ul>
