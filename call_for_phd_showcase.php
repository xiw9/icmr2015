            <p>Submissions must be written in English and submitted in the ACM Conference style (for LaTeX, use the “Option 2″ style). The paper should be no more than <em>4 pages</em> in length including all figures and references. The first page must contain the title of the paper, full author name, affiliation and contact details, an abstract of up to 250 words, ACM Computing Reviews categories, and up to 3 keywords describing the topic areas. Information about categories and keywords can be found in the ACM Web pages on the computing classification system and in the LaTeX and Word templates.</p>
            <h3>Review Process and Publication</h3>
            <p>Review is single-blind. All submissions will be reviewed by the members of the committee and will be judged with respect to their novelty, significance, potential for future impact of the research on the field and the quality of the presentation.</p>
            <p>The 4-page submission will be published in a separate section in an electronic proceedings and will be distributed at the conference.</p>
            <p>Student authors are expected to present their work in the conference in two parallel sessions during the conference. Only selected senior researchers and students attend these sessions with the sole aim to give friendly constructive feedback to the students.</p>
